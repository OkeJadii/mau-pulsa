    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Transaction  $transaction
     * @return \Illuminate\Http\Response
     */
    public function show(Transaction $transaction)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Transaction  $transaction
     * @return \Illuminate\Http\Response
     */
    public function edit(Transaction $transaction)
    {
        return view('admin.transaction.edit', [
            'title' => 'Edit Data Transaksi',
            'transaction' => $transaction
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Transaction  $transaction
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Transaction $transaction)
    {
        $rules = [
            'produk' => 'required',
            'nominal' => 'required',
            'harga' => 'required',
            'status' => 'required'
        ];

        $validatedData = $request->validate($rules);

        Transaction::where('id', $transaction->id)
            ->update($validatedData);

        return redirect('/admin/transaction')->with('success', '<strong>Data Transaksi Berhasil Di Ubah!</strong>');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Transaction  $transaction
     * @return \Illuminate\Http\Response
     */
    public function destroy(Transaction $transaction)
    {
        Transaction::destroy($transaction->id);

        return redirect('/admin/transaction')->with('success', '<strong>Data Transaksi Telah Berhasil Terhapus!</strong>');;
    }
}
